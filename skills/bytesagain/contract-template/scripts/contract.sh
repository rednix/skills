show_help() {
    cat <<'EOF'
合同模板生成器 - contract.sh

用法：
  contract.sh labor   "甲方" "乙方"                劳动合同模板
  contract.sh nda     "甲方" "乙方"                保密协议NDA
  contract.sh service "甲方" "乙方" "服务内容"      服务协议
  contract.sh rental  "房东" "租客" "地址"          租赁合同
  contract.sh help                                  显示本帮助

示例：
  contract.sh labor "北京科技有限公司" "张三"
  contract.sh nda "甲方公司" "乙方公司"
  contract.sh service "委托方" "服务方" "软件开发"
  contract.sh rental "李四" "王五" "朝阳区XX路XX号"

⚠️  本工具生成的合同模板仅供参考，不构成法律建议。
    正式签署前请咨询专业律师。
EOF
}

